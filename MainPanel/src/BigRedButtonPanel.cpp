#include "BigRedButtonPanel.h"

//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
BigRedButtonPanel::BigRedButtonPanel(Ring& ring)
  : Panel(ring)
{
}

//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
void BigRedButtonPanel::initializeBigRedButton()
{
  clearStates();

  mRedState = true;

  writeLeds();

}

//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
bool BigRedButtonPanel::bigRedButtonMode()
{
  if (digitalRead(mRedButtonPin))
  {
    return true;
  }
  return false;
}
