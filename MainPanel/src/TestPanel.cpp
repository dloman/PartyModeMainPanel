#include "TestPanel.h"

//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
TestPanel::TestPanel(Ring& ring)
  : Panel(ring)
{
}

//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
void TestPanel::run()
{
  if (isPanelOn())
  {
    getSwitchStates();
    writeLeds();
    readAndWriteSiren();
    mRing.colorRing(50);
  }
  else
  {
    clearStates();

    writeLeds();
    mRing.clearRing();
  }
}
